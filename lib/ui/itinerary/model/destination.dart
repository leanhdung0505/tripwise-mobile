class Destination {
  final String name;
  final String country;
  final int days;
  final String startDate;
  final String endDate;
  final List<String> images;

  Destination({
    required this.name,
    required this.country,
    required this.days,
    required this.startDate,
    required this.endDate,
    required this.images,
  });
}